def green(text):
    return "\033[32m{}\033[0m".format(text)

def blue(text):
    return "\033[34m{}\033[0m".format(text)

def yellow(text):
    return "\033[33m{}\033[0m".format(text)

def red(text):
    return "\033[31m{}\033[0m".format(text)

def white(text):
    return "\033[37m{}\033[0m".format(text)